#define YELLOW 0xFF,0xFF,0x0
#define BLUE 0x0,0x0,0xFF
#define MAGENTA 0xFF,0,0xFF
#define CYAN 0,0xFF,0xFF
#define WHITE 0xFF,0xFF,0xFF

/* other colors */
#define ORANGE  0xff, 0x66, 0x00
#define GREEN2 0x0,0x80,0x0
#define BLUE_NICE 0x0,0x33,0xAA
#define GRAY_LIGHT 0x66,0x66,0x66
#define GRAY_DARK 0x33,0x33,0x33
#define NORTON_UTILITIES_VIOLET 0x55,0x55,0xFF


#include "rgb.h"
#define RGB3(x) RGB(x)

